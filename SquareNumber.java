public class SquareNumber {
	public void findSquare(int val1) {
		int squareValue=val1*val1;
		System.out.println(squareValue);
	}
	public void findSquare(int val2,int val3) {
		int addedValue=val2+val3;
		int squareValue=addedValue*addedValue;
		System.out.println(squareValue);
	}

}
